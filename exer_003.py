#Crie um programa que leia dois numeros
#e mostre a soma entre eles

# Solicita ao usuário que digite o primeiro número e converte para inteiro
num1 = int(input("digite o primeiro numero: "))

# Solicita ao usuário que digite o segundo número e converte para inteiro
num2 = int(input("digite o segundo numero: "))

# Realiza a soma dos dois números fornecidos pelo usuário
soma = num1 + num2

# Exibe o resultado da soma, mostrando os dois números e o valor total
print(f"a soma entre {num1} e {num2} vale {soma}")
